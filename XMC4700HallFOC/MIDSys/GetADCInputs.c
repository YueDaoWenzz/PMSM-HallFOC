 /*
Iu====P2.11====G1CH3  +  Iv====P2.10=====G0Ch3
Iw====P2.3=====G1CH5
EMFW==P2.8=====G1CH0  +		EMFV==P2.6======G0CH0
UDC===P2.4=====G1CH6
POT===P2.5=====G1CH7	+		EMFU==P2.2====G0CH7
*/

int16_t  Read_POT(void)  //电位器
{
			int16_t	POT_result;
	
			POT_result	=	(VADC_G1->RES[7] & 0x0fff);
	
			if(POT_result>1024){POT_result-=1024;}  //减去基值
			else{POT_result=0;}   

			if(POT_result>=2048)  //最大值
			{
				POT_result=2048;
			}
			POT_result <<=1;
			return  ((int16_t)POT_result);
       
}
int16_t  Read_UDC(void)  //母线
{
			return  ((int16_t)(VADC_G1->RES[6] & 0x0fff));
     
}
int16_t  Read_BEMF_U(void) //U反电动势
{
			return  ((int16_t)(VADC_G0->RES[7] & 0x0fff));
       
}
int16_t  Read_BEMF_V(void) //V反电动势
{
			return  ((int16_t)(VADC_G0->RES[0] & 0x0fff));
      
}
int16_t  Read_BEMF_W(void) //W反电动势
{

			return  ((int16_t)(VADC_G1->RES[0] & 0x0fff));
      
}
